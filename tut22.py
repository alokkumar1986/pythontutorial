vowel = input('Enter your character : ')
if vowel == 'A' or vowel == 'a':
    print('The character is : A/a')
elif vowel == 'E' or vowel == 'e':
    print('The character is : E/e')
elif vowel == 'I' or vowel == 'i':
    print('The character is : I/i')
elif vowel == 'O' or vowel == 'o':
    print('The character is : O/o')
elif vowel == 'U' or vowel == 'u':
    print('The character is : U/u')
else :
    print('The character is not a vowel')